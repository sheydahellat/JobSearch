public class Job {
    public String town;
    int salary;
    Boolean senior;
    Boolean junior;
    Boolean partTime;
    Boolean fullTime;
    Boolean intern;
    public Job(String town, int salary, Boolean senior, Boolean junior, Boolean partTime, Boolean fullTime, Boolean intern) {
        this.town = town;
        this.salary = salary;
        this.senior = senior;
        this.junior = junior;
        this.partTime = partTime;
        this.fullTime = fullTime;
        this.intern = intern;
    }
}

